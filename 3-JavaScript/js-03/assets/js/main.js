/* Estructura que nos permite repetir nuestro código */


/* for */
/* Es un ciclo controlado */

/* se le tiene que indicar lo siguiente:
    1.- la variable con la que va a trabajar
    2.- siempre se utiliza la i, j, k, l, 
    3.- condición para que se ejecute
    4.- el cambio de nuestra variable de trabajo
    */

for(let i = 0; i < 10; i++ ) {
    console.log("El valor de i es " + i);
}


/* while */
/* Es un ciclo no controlado */

/* se necesita:     
    1.- una condicion (un true o un false)
*/

